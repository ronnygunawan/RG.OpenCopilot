namespace Calculator;

/// <summary>
/// A simple calculator with basic arithmetic operations.
/// </summary>
public sealed class Calculator {
    /// <summary>
    /// Adds two numbers.
    /// </summary>
    public int Add(int a, int b) {
        return a + b;
    }

    /// <summary>
    /// Subtracts b from a.
    /// </summary>
    public int Subtract(int a, int b) {
        return a - b;
    }
}
